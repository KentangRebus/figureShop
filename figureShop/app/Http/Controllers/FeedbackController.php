    public function approve($id){
        $feed = Feedback::find($id);
        $feed->status = 'approved';
        $feed->save();

        return redirect('/Admin/Feedback');
    }

    public function reject($id){
        $feed = Feedback::find($id);
        $feed->status = 'rejected';
        $feed->save();

        return redirect('/Admin/Feedback');
    }

